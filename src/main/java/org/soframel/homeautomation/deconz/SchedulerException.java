package org.soframel.homeautomation.deconz;

public class SchedulerException  extends Exception{

    public SchedulerException() {
    }

    public SchedulerException(String arg0) {
        super(arg0);
    }

    public SchedulerException(Throwable arg0) {
        super(arg0);
    }

    public SchedulerException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public SchedulerException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
        super(arg0, arg1, arg2, arg3);
    }
    
}
